// Tests
// ---------------------------------------------------------------------------

const ALL_LAYERS = Object.keys(LAYER_TO_NAMESPACE);

describe('fileToLeaf — entities layer', () => {
  it('recognizes entities flat file', () => {
    const leaf = fileToLeaf('entities/users.tsx', ALL_LAYERS);
    expect(leaf).not.toBeNull();
    expect(leaf!.layer).toBe('entities');
    expect(leaf!.importPath).toBe('@entities/users');
    expect(leaf!.segments).toEqual(['Users']);
  });

  it('recognizes entities nested file', () => {
    const leaf = fileToLeaf('entities/users/profile.tsx', ALL_LAYERS);
    expect(leaf).not.toBeNull();
    expect(leaf!.layer).toBe('entities');
    expect(leaf!.importPath).toBe('@entities/users/profile');
    expect(leaf!.segments).toEqual(['Users', 'Profile']);
  });

  it('ignores dot-files inside entities', () => {
    const leaf = fileToLeaf('entities/.gitkeep', ALL_LAYERS);
    expect(leaf).toBeNull();
  });

  it('ignores files not in a recognized layer', () => {
    const leaf = fileToLeaf('pages/auth.tsx', ALL_LAYERS);
    expect(leaf).toBeNull();
  });
});

describe('renderRuntime — entities flat file', () => {
  const leaf = fileToLeaf('entities/users.tsx', ALL_LAYERS)!;

  it('emits eager import at the top of the file', () => {
    const out = renderRuntime([leaf]);
    expect(out).toContain("import _Users from '@entities/users';");
  });

  it('does NOT use lazy() for entity leaf', () => {
    const out = renderRuntime([leaf]);
    expect(out).not.toContain("lazy(() => import('@entities/users'))");
  });

  it('emits Entities export referencing the import variable', () => {
    const out = renderRuntime([leaf]);
    expect(out).toContain('export const Entities = {');
    expect(out).toContain('Users: _Users');
  });
});

describe('renderRuntime — entities nested namespace', () => {
  const leaf = fileToLeaf('entities/users/profile.tsx', ALL_LAYERS)!;

  it('emits eager import with underscore-joined variable name', () => {
    const out = renderRuntime([leaf]);
    expect(out).toContain("import _Users_Profile from '@entities/users/profile';");
  });

  it('emits nested namespace Entities.Users.Profile', () => {
    const out = renderRuntime([leaf]);
    expect(out).toContain('Users: {');
    expect(out).toContain('Profile: _Users_Profile');
  });
});

describe('renderRuntime — empty entities folder', () => {
  it('emits empty Entities object (no crash, no lazy)', () => {
    const out = renderRuntime([]);
    expect(out).toContain('export const Entities = {};');
    expect(out).not.toContain("lazy(() => import('@entities");
  });
});

describe('renderRuntime — existing layers still use lazy (regression)', () => {
  const widgetLeaf = fileToLeaf('widgets/forms/auth.tsx', ALL_LAYERS)!;

  it('widget leaf uses lazy(), not eager import', () => {
    const out = renderRuntime([widgetLeaf]);
    expect(out).toContain("lazy(() => import('@widgets/forms/auth'))");
    expect(out).not.toContain('import _Forms_Auth');
  });

  it('Widgets namespace is present', () => {
    const out = renderRuntime([widgetLeaf]);
    expect(out).toContain('export const Widgets = {');
  });
});

describe('renderRuntime — multiple entities sorted and ordered', () => {
  const leaf1 = fileToLeaf('entities/products.tsx', ALL_LAYERS)!;
  const leaf2 = fileToLeaf('entities/users.tsx', ALL_LAYERS)!;

  it('emits both eager imports', () => {
    const out = renderRuntime([leaf1, leaf2]);
    expect(out).toContain("import _Products from '@entities/products';");
    expect(out).toContain("import _Users from '@entities/users';");
  });

  it('Entities object contains both keys', () => {
    const out = renderRuntime([leaf1, leaf2]);
    expect(out).toContain('Products: _Products');
    expect(out).toContain('Users: _Users');
  });
});

describe('renderTypes — entities interface', () => {
  it('flat entity generates correct typeof import type', () => {
    const leaf = fileToLeaf('entities/users.tsx', ALL_LAYERS)!;
    const out = renderTypes([leaf]);
    expect(out).toContain('interface Entities {');
    expect(out).toContain("Users: typeof import('@entities/users').default;");
  });

  it('nested entity generates nested interface structure', () => {
    const leaf = fileToLeaf('entities/users/profile.tsx', ALL_LAYERS)!;
    const out = renderTypes([leaf]);
    expect(out).toContain('interface Entities {');
    expect(out).toContain('Users:');
    expect(out).toContain("Profile: typeof import('@entities/users/profile').default;");
  });

  it('empty entities generates empty interface (not omitted)', () => {
    const out = renderTypes([]);
    expect(out).toContain('interface Entities {}');
  });

  it('output is a valid global augmentation shape', () => {
    const leaf = fileToLeaf('entities/users.tsx', ALL_LAYERS)!;
    const out = renderTypes([leaf]);
    expect(out).toContain('declare global {');
    expect(out).toContain('}');
    expect(out).toContain('export {};');
  });
});
